package gh.funthomas424242.algorithmen.datum;

public class Abstand {
	
	protected int monate;
	
	protected int tage;
	
	public Abstand(final int monate, final int tage){
		this.monate=monate;
		this.tage=tage;
	}

	public int getMonate() {
		return monate;
	}

	public int getTage() {
		return tage;
	}
	
	

}
